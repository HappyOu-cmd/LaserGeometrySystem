    def _wait_for_ip_address(self, ip_address, max_retries=30, retry_delay=1):
        """–û–∂–∏–¥–∞–Ω–∏–µ –≥–æ—Ç–æ–≤–Ω–æ—Å—Ç–∏ IP –∞–¥—Ä–µ—Å–∞"""
        for attempt in range(max_retries):
            try:
                # –ü—ã—Ç–∞–µ–º—Å—è —Å–æ–∑–¥–∞—Ç—å —Å–æ–∫–µ—Ç –∏ –ø—Ä–∏–≤—è–∑–∞—Ç—å –µ–≥–æ –∫ IP –∞–¥—Ä–µ—Å—É
                test_sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
                test_sock.setsockopt(socket.SOL_SOCKET, socket.SO_REUSEADDR, 1)
                test_sock.bind((ip_address, 0))  # –ü–æ—Ä—Ç 0 = –ª—é–±–æ–π —Å–≤–æ–±–æ–¥–Ω—ã–π –ø–æ—Ä—Ç
                test_sock.close()
                return True
            except (OSError, socket.error) as e:
                if attempt < max_retries - 1:
                    time.sleep(retry_delay)
                else:
                    self.log_message(f"IP –∞–¥—Ä–µ—Å {ip_address} –Ω–µ –≥–æ—Ç–æ–≤ –ø–æ—Å–ª–µ {max_retries} –ø–æ–ø—ã—Ç–æ–∫: {e}")
                    return False
        return False
    
    def run_modbus_server(self, port, identity):
        """–ó–∞–ø—É—Å–∫ Modbus —Å–µ—Ä–≤–µ—Ä–∞ —Å retry –ª–æ–≥–∏–∫–æ–π"""
        ip_address = "192.168.1.50"
        max_retries = 10
        retry_delay = 2
        
        for attempt in range(max_retries):
            try:
                # –ü—Ä–æ–≤–µ—Ä—è–µ–º –≥–æ—Ç–æ–≤–Ω–æ—Å—Ç—å IP –∞–¥—Ä–µ—Å–∞ –ø–µ—Ä–µ–¥ –∑–∞–ø—É—Å–∫–æ–º
                if not self._wait_for_ip_address(ip_address, max_retries=5, retry_delay=0.5):
                    if attempt < max_retries - 1:
                        self.log_message(f"–ü–æ–ø—ã—Ç–∫–∞ {attempt + 1}/{max_retries}: IP –∞–¥—Ä–µ—Å {ip_address} –Ω–µ –≥–æ—Ç–æ–≤, –ø–æ–≤—Ç–æ—Ä —á–µ—Ä–µ–∑ {retry_delay}—Å...")
                        time.sleep(retry_delay)
                        continue
                    else:
                        self.log_message(f"–ö–†–ò–¢–ò–ß–ï–°–ö–ê–Ø –û–®–ò–ë–ö–ê: IP –∞–¥—Ä–µ—Å {ip_address} –Ω–µ –≥–æ—Ç–æ–≤ –ø–æ—Å–ª–µ –≤—Å–µ—Ö –ø–æ–ø—ã—Ç–æ–∫!")
                        return
                
                self.log_message(f"–ó–∞–ø—É—Å–∫ TCP —Å–µ—Ä–≤–µ—Ä–∞ –Ω–∞ {ip_address}:{port} (–ø–æ–ø—ã—Ç–∫–∞ {attempt + 1}/{max_retries})")
                
                # –î–ª—è pymodbus 3.5.4 - —É–ø—Ä–æ—â–µ–Ω–Ω—ã–π —Å–∏–Ω—Ç–∞–∫—Å–∏—Å
                StartTcpServer(
                    context=self.server_context, 
                    identity=identity, 
                    address=(ip_address, port)
                )
                # –ï—Å–ª–∏ –¥–æ—à–ª–∏ —Å—é–¥–∞, —Å–µ—Ä–≤–µ—Ä –∑–∞–ø—É—Å—Ç–∏–ª—Å—è —É—Å–ø–µ—à–Ω–æ
                return
                
            except OSError as e:
                error_msg = str(e).lower()
                if "address already in use" in error_msg or "port" in error_msg:
                    self.log_message(f"–ü–æ—Ä—Ç {port} –∑–∞–Ω—è—Ç, –ø–æ–≤—Ç–æ—Ä —á–µ—Ä–µ–∑ {retry_delay}—Å... (–ø–æ–ø—ã—Ç–∫–∞ {attempt + 1}/{max_retries})")
                    if attempt < max_retries - 1:
                        time.sleep(retry_delay)
                        continue
                    else:
                        self.log_message(f"–ö–†–ò–¢–ò–ß–ï–°–ö–ê–Ø –û–®–ò–ë–ö–ê: –ü–æ—Ä—Ç {port} –∑–∞–Ω—è—Ç –ø–æ—Å–ª–µ –≤—Å–µ—Ö –ø–æ–ø—ã—Ç–æ–∫!")
                        return
                elif "cannot assign requested address" in error_msg or "bind" in error_msg:
                    self.log_message(f"IP –∞–¥—Ä–µ—Å {ip_address} –Ω–µ –≥–æ—Ç–æ–≤, –ø–æ–≤—Ç–æ—Ä —á–µ—Ä–µ–∑ {retry_delay}—Å... (–ø–æ–ø—ã—Ç–∫–∞ {attempt + 1}/{max_retries})")
                    if attempt < max_retries - 1:
                        time.sleep(retry_delay)
                        continue
                    else:
                        self.log_message(f"–ö–†–ò–¢–ò–ß–ï–°–ö–ê–Ø –û–®–ò–ë–ö–ê: –ù–µ —É–¥–∞–ª–æ—Å—å –ø—Ä–∏–≤—è–∑–∞—Ç—å—Å—è –∫ {ip_address}:{port} –ø–æ—Å–ª–µ –≤—Å–µ—Ö –ø–æ–ø—ã—Ç–æ–∫!")
                        return
                else:
                    self.log_message(f"–û—à–∏–±–∫–∞ —Ä–∞–±–æ—Ç—ã —Å–µ—Ä–≤–µ—Ä–∞: {e}")
                    if attempt < max_retries - 1:
                        time.sleep(retry_delay)
                        continue
                    else:
                        self.log_message(f"–ö–†–ò–¢–ò–ß–ï–°–ö–ê–Ø –û–®–ò–ë–ö–ê: {e}")
                        return
            except Exception as e:
                self.log_message(f"–û—à–∏–±–∫–∞ —Ä–∞–±–æ—Ç—ã —Å–µ—Ä–≤–µ—Ä–∞: {e}")
                print(f"–î–µ—Ç–∞–ª—å–Ω–∞—è –æ—à–∏–±–∫–∞: {e}")  # –î–ª—è –æ—Ç–ª–∞–¥–∫–∏
                if attempt < max_retries - 1:
                    time.sleep(retry_delay)
                    continue
                else:
                    return
            
