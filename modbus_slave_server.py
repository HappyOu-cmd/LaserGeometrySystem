    def monitor_registers(self):
        """–ú–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥ –∏–∑–º–µ–Ω–µ–Ω–∏–π –≤ —Ä–µ–≥–∏—Å—Ç—Ä–∞—Ö"""
